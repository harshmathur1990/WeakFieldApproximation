def generate_original_drift_signal(value1, value2, time_in_seconds, seeing_arcsec, drift_1_arcsec, drift_2_arcsec):
    # noise = seeing_arcsec * 223 * seeing / 5.5
    drift_1 = drift_1_arcsec * 223 / (5.5 * 60 * 1000)
    drift_2 = drift_2_arcsec * 223 / (5.5 * 60 * 1000)

    ori_list1, ori_list2 = list(), list()

    for t in range(time_in_seconds * 1000):
        value1 += drift_1 * (1 + seeing[t%1000])
        value2 += drift_2 * (1 + seeing[t%1000])
        ori_list1.append(value1)
        ori_list2.append(value2)

    return np.array(ori_list1), np.array(ori_list2)


